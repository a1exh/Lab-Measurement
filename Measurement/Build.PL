# Some instruments are available under multiple names.

use 5.010;
use File::Spec::Functions qw/catfile/;
use autodie;

my %aliases = (
	Agilent34410A => [qw/HP34410A Keysight34410A/],
    );

sub create_alias {
	my $key = shift;
	my @aliases = @{scalar shift};
	foreach my $alias (@aliases) {
		
		my $parent = "Lab::Instrument::$key";
		my $instrument = "Lab::Instrument::$alias";
		
		my $content = <<"EOF";
# Do not edit. Auto-generated by Build.PL.
package $instrument;
use warnings;
use strict;

use parent qw/$parent/;

1;

=pod

=head1 NAME

$instrument.
This is an alias for L<$parent>.

=cut

EOF
		say "creating alias '$instrument' for '$parent'";
		open my $fh, ">", catfile(qw/lib Lab Instrument/, "$alias.pm");
		
		print {$fh} $content;

		close $fh;
	}
}
		
		
foreach my $key (keys %aliases) {
	create_alias($key, $aliases{$key});
}

$build->create_build_script;
